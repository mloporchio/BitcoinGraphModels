#!/bin/bash

EXPERIMENT_NAME=$1
MODEL_NAME=$2

INPUT_FILE="${MODEL_NAME}/el.tsv"
OUTPUT_FILE="${MODEL_NAME}/${EXPERIMENT_NAME}.tsv"
LOG_OUTPUT="log/${EXPERIMENT_NAME}_output_${MODEL_NAME}.log"
LOG_ERROR="log/${EXPERIMENT_NAME}_error_${MODEL_NAME}.log"

eval "./${EXPERIMENT_NAME} ${INPUT_FILE} ${OUTPUT_FILE} 1>${LOG_OUTPUT} 2>${LOG_ERROR}"